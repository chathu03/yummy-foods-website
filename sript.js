// pizza
const pizzaItem = document.getElementById("pizza");

pizzaItem.addEventListener("mouseover", function () {
  pizzaItem.style.fontSize = "24px";
});

pizzaItem.addEventListener("mouseout", function () {
  pizzaItem.style.fontSize = "";
});

